let operandOne = 'test';
let operandTwo = 4;
// operand = 피연산자
let result = (operandOne.length === operandTwo) ? "hello" : "bye";

console.log(result); // hello

// 삼항연산자(ternary operator): if-else 구문과 같다
// 위 예제의 내용은 '만약 operandOne 값의 길이가 operandTwo와 같으면 "hello"를 출력하고, 그렇지 않을 경우 "bye"를 출력하라'는 의미이다

